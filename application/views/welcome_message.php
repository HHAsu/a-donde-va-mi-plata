<a name="user-content-para-quien" class="anchor" href="#para-quien"><span class="octicon octicon-link"></span></a>Para quien?</h2>

<p>Explique a quién está dirigido, cuáles son sus usuarios finales y cuáles son sus características distintivas.
Dado que el Presupuesto General es una herramienta de política gubernativa, mediante la cual se asignan recursos y determinan gastos para el cumplimiento de objetivos y políticas de estado para el desarrollo nacional en todas sus formas. La iniciativa se dirige a toda la ciudadanía en general.
Esta herramienta es de puntual utilidad para investigadores, agencias de cooperación internacional, agentes tomadores de decisión en ambientes tanto públicos como privados, formadores de opinión, periodistas, comunicadores, analistas, estudiantes, organizaciones no gubernamentales, y ciudadanos y ciudadanas constructores y constructoras de una sociedad participativa e informada.</p>

<h2>
<a name="user-content-objetivos" class="anchor" href="#objetivos"><span class="octicon octicon-link"></span></a>Objetivos</h2>

<p>¿A dónde va mi plata? tiene por objetivo facilitar a la ciudadanía el acceso y el análisis comparativo de los datos del presupuesto general de la Nación con una herramienta Web que visualice la información de forma simple organizada en tiempo y funciones estatales.</p>

<h2>
<a name="user-content-que-queremos" class="anchor" href="#que-queremos"><span class="octicon octicon-link"></span></a>Que queremos</h2>

<p>Los/las usuarios/as de ¿A dónde va mi plata? podrán visualizar y comparar de qué manera se gastan los recursos del presupuesto en distintas áreas claves y en distintos años, pudiendo asi realizar distintos análisis y adquirir conciencia sobre el gasto público, a través de una interfaz web sencilla con reportes gráficos basados en técnicas de visualización de datos.</p>

<h2>
<a name="user-content-porque" class="anchor" href="#porque"><span class="octicon octicon-link"></span></a>Porque?</h2>

<p>Existen datos del presupuesto general de la nación de manera pública, pero no son amigables, y son desconocidas por la ciudadanía en general. Esta nueva herramienta permitirá realizar consultas puntuales, así como también analizar, interpretar, examinar e investigar acerca de las tendencias de dichos gastos. Es la primera experiencia de visualización de datos públicos referentes a gastos del Estado paraguayo, con periodismo de dato que a través de un grupo multidiciplinario que es la incipiente comunidad Hacks Hackers Asunción.</p>

